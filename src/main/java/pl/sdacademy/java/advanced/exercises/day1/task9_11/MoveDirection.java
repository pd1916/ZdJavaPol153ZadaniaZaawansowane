package pl.sdacademy.java.advanced.exercises.day1.task9_11;

public class MoveDirection {
    private final double x;
    private final double y;

    public MoveDirection(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
